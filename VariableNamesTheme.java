        String typeOfMessage = "numeric";
        System.out.println("\nтип сообщения - " + typeOfMessage);

        // число нулей
        int zeros = 2;
        System.out.println("\nчисло нулей - " + zeros);

        // уникальное число
        int uniqueNum = 73;
        System.out.println("\nуникальное число - " + uniqueNum);

        // случайное число
        int randomNum = (int) (Math.random() * 1000);
        System.out.println("\nслучайное число - " + randomNum);

        // математическое выражение
        int mathExpression = 2 * 4;
        System.out.println("\nматематическое выражение - " + mathExpression);

        // выбор (чего-либо)
        String choice = "что-то";
        System.out.println("\nвыбор (чего-либо) - " + choice);

        // счёт в игре
        String score = "10 - 3";
        System.out.println("\nсчёт в игре - " + score);

        // максимальная длина (строки)
        int maxLengthLine = 5;
        System.out.println("\nмаксимальная длина (строки) - " + maxLengthLine);

        // пункт меню
        double menuItem = 2.0;
        System.out.println("\nпункт меню - " + menuItem);

        // стоимость кофе на вынос
        double costCoffeeToTakeAway = 3.55;
        System.out.println("\nстоимость кофе на вынос - " + costCoffeeToTakeAway);
        System.out.println();

        // дата начала (чего-либо)
        String startDate = "01.01.2024";
        System.out.println("\nдата = " + startDate);

        // окончание диапазона
        String endRange = "$E$6E13";
        System.out.println("\nокончание диапозона - " + endRange);

        // полное имя работника месяца
        String fullNameEmployeeOfMonth = "Александр";
        System.out.println("\nполное имя работника месяца - " + fullNameEmployeeOfMonth);

        // заголовок электронной книги
        String ebookTitle = "Приключения";
        System.out.println("\nзаголовок электронной книги - " + ebookTitle);

        // размер
        String size = "L";
        System.out.println("\nразмер - " + size);

        // вместимость
        int capacity = 12;
        System.out.println("\nвместимость - " + capacity);

        // счётчик
        double counter = 10;
        System.out.println("\nсчётчик - " + counter);

        // путь до файла
        String path = "C:\\Desktop\\";
        System.out.println("\nпуть до файла - " + path);

        // количество чисел в каждой строке
        int countNumsPerLines = 5;
        System.out.println("\nколичество чисел в каждой строке - " + countNumsPerLines);

        // сотни равны?
        boolean isHundredsEqual = true;
        System.out.println("\nсотни равны? - " + isHundredsEqual);

        // компьютер включен?
        boolean isComputerTurnedOn = true;
        System.out.println("\nкомпьютер включен? - " + isComputerTurnedOn);

        // есть равные цифры?
        boolean hasNumsEqual = true;
        System.out.println("\nесть равные цифры? - " + hasNumsEqual);

        // (что-либо) создано?
        boolean hasCreated = true;
        System.out.println("\n(что-либо) создано? - " + hasCreated);

        // (что-либо) пустое?
        boolean isEmpty = true;
        System.out.println("\n(что-либо) пустое? - " + isEmpty);

        // (что-либо) активное?
        boolean isActive = true;
        System.out.println("\n(что-либо) активное? - " + isActive);

        // (что-либо) новое?
        boolean isNew = true;
        System.out.println("\n(что-либо) новое? - " + isNew);

